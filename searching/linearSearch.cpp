bool linearSearch(int arr[], int size, int key)
{
    for (int i = 0; i < size; i++)
    {
        if (arr[i] == key)
        {
            return true;
        }
    }
    return false;
}
int main()
{
    int arr[] = {1, 8, 6, 4, 12, 78};
    int size = 6;
    int key;
    cout << "Enter the key to be searched : ";
    cin >> key;
    if (linearSearch(arr, size, key))
    {
        cout << "found ";
    }
    else
    {
        cout << "not found ";
    }
    return 0;
}